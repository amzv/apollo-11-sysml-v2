def find_defining_package(element_name: str, resolver: SysMLDependencyResolver) -> Optional[str]:
    """
    Find which package defines the given element by searching the package
    files or matching the package name directly.
    """
    if element_name in resolver.package_index:
        return element_name

    # Regex pattern: look for "def ElementName" or "package ElementName"
    pattern = re.compile(rf"\b(?:def|package)\s+['\"]?{re.escape(element_name)}['\"]?\b")

    for pkg_name, rel_path in resolver.package_index.items():
        abs_path = os.path.join(resolver.repo_path, rel_path)
        if os.path.isfile(abs_path):
            try:
                with open(abs_path, "r", encoding="utf-8", errors="ignore") as f:
                    content = f.read()
                if pattern.search(content):
                    return pkg_name
            except Exception:
                pass
    return None


def run_validation(
    repo_path: str,
    output_dir: str,
    viz_elements: List[Tuple[str, str]],
    timeout: int,
    dry_run: bool = False,
    continue_on_error: bool = True,
    target_package: Optional[str] = None,
) -> ValidationReport:
    """
    Main validation workflow:
    1. Resolve dependencies and determine loading order
    2. Start the SysML kernel (skipped in dry-run mode)
    3. Load each file via %publish, verify with %show, and interleave %viz
    4. Generate report and notebook
    """
    from datetime import datetime, timezone

    repo_path = os.path.abspath(repo_path)
    output_dir = os.path.abspath(output_dir)
    os.makedirs(output_dir, exist_ok=True)

    report = ValidationReport(
        timestamp=datetime.now(timezone.utc).isoformat(),
        repo_path=repo_path,
    )

    # -----------------------------------------------------------------------
    # Step 1: Resolve dependencies
    # -----------------------------------------------------------------------
    logger.info("=" * 60)
    logger.info("STEP 1: Resolving dependencies")
    logger.info("=" * 60)

    resolver = SysMLDependencyResolver(repo_path)
    loading_order = resolver.resolve()

    if target_package:
        # Check if the target is actually a file path or package name
        target_pkg_name = None
        for pkg, path in resolver.package_index.items():
            if (target_package == pkg or 
                os.path.basename(target_package) == os.path.basename(path) or 
                target_package == path or
                os.path.normpath(target_package) == os.path.normpath(path)):
                target_pkg_name = pkg
                break
        
        if not target_pkg_name:
            raise ValueError(f"Target package or file '{target_package}' not found in index.")
        
        target_package = target_pkg_name
        logger.info("Filtering dependencies for target package: %s", target_package)
        
        # Traverse dependencies recursively
        needed = set()
        queue = [target_package]
        while queue:
            curr = queue.pop(0)
            if curr not in needed:
                needed.add(curr)
                queue.extend(resolver.dep_graph.get(curr, set()))
        
        loading_order = [item for item in loading_order if item[0] in needed]
        if not loading_order:
            raise ValueError(f"No packages resolved for target '{target_package}'.")

    report.total_files = len(loading_order)
    report.loading_order = [name for name, _ in loading_order]

    logger.info("Loading order (%d files):", len(loading_order))
    for i, (name, path) in enumerate(loading_order, 1):
        logger.info("  %2d. %s", i, name)

    # Map visualization elements to their defining packages
    pkg_to_viz = defaultdict(list)
    fallback_viz = []

    if viz_elements:
        logger.info("Mapping visualization targets to defining packages...")
        for element_name, view_type in viz_elements:
            defining_pkg = find_defining_package(element_name, resolver)
            if defining_pkg:
                pkg_to_viz[defining_pkg].append((element_name, view_type))
                logger.info("  Mapped '%s:%s' to package '%s'", element_name, view_type, defining_pkg)
            else:
                fallback_viz.append((element_name, view_type))
                logger.warning("  Could not map '%s:%s' to any package (will execute as fallback)", element_name, view_type)

    # -----------------------------------------------------------------------
    # Step 2: Dry-run check
    # -----------------------------------------------------------------------
    if dry_run:
        logger.info("")
        logger.info("=" * 60)
        logger.info("DRY RUN — Skipping kernel execution")
        logger.info("=" * 60)
        logger.info("Dependency resolution succeeded. %d files would be loaded.", len(loading_order))
        for i, (name, path) in enumerate(loading_order, 1):
            rel = os.path.relpath(path, repo_path)
            logger.info("  %2d. %s  (%s)", i, name, rel)
        if viz_elements:
            viz_str = ", ".join(f"{n} --view {v}" for n, v in viz_elements)
            logger.info("Visualization targets: %s", viz_str)
        report.total_time_s = round(time.time() - time.time(), 2)
        return report

    # -----------------------------------------------------------------------
    # Step 2: Start kernel and validate
    # -----------------------------------------------------------------------
    logger.info("")
    logger.info("=" * 60)
    logger.info("STEP 2: Starting kernel and validating files")
    logger.info("=" * 60)

    validator = SysMLKernelValidator(timeout=timeout)
    overall_start = time.time()

    # Track which packages failed, so dependents can be skipped
    failed_packages: set = set()

    try:
        validator.start_kernel()

        # Load each file in dependency order
        for i, (pkg_name, file_path) in enumerate(loading_order, 1):
            rel_path = os.path.relpath(file_path, repo_path)

            # Check if any dependency of this package failed
            if continue_on_error:
                pkg_deps = resolver.dep_graph.get(pkg_name, set())
                failed_deps = pkg_deps & failed_packages
                if failed_deps:
                    logger.warning(
                        "[%d/%d] SKIPPING %s — depends on failed: %s",
                        i, len(loading_order), pkg_name,
                        ", ".join(sorted(failed_deps)),
                    )
                    skip_result = FileResult(
                        package_name=pkg_name,
                        file_path=file_path,
                        status="skipped",
                        errors=[f"Skipped: dependency failed ({', '.join(sorted(failed_deps))})"],
                    )
                    report.file_results.append(skip_result)
                    report.skipped += 1
                    failed_packages.add(pkg_name)
                    continue

            logger.info(
                "[%d/%d] Loading %s (%s)...",
                i, len(loading_order), pkg_name, rel_path,
            )

            file_result = validator.validate_file(pkg_name, file_path)
            report.file_results.append(file_result)

            if file_result.status == "pass":
                report.passed += 1
                method_info = f" via %{file_result.load_method}" if file_result.load_method else ""
                logger.info(
                    "  ✅ OK (%.1fs%s)", file_result.execution_time_s, method_info
                )

                # Interleaved visualization: run %viz commands for this package immediately!
                if pkg_name in pkg_to_viz:
                    for element_name, view_type in pkg_to_viz[pkg_name]:
                        viz_result = validator.run_viz(element_name, output_dir, view_type)
                        viz_result.package_name = pkg_name
                        report.viz_results.append(viz_result)

                        if viz_result.status == "pass":
                            saved_to = (
                                viz_result.svg_path or viz_result.html_path
                                or viz_result.pdf_path or viz_result.png_path
                            )
                            logger.info("  ✅ Viz: %s (%s) → %s", element_name, view_type, saved_to)
                        else:
                            for err in viz_result.errors:
                                logger.warning("  ⚠️ Viz: %s (%s): %s", element_name, view_type, err)
            else:
                report.failed += 1
                failed_packages.add(pkg_name)
                for err in file_result.errors:
                    logger.error("  ❌ %s", err)

                if not continue_on_error:
                    logger.error(
                        "Stopping: --no-continue-on-error is set and %s failed.",
                        pkg_name,
                    )
                    break

        # -------------------------------------------------------------------
        # Step 3: Fallback/Remaining Visualizations
        # -------------------------------------------------------------------
        if fallback_viz:
            logger.info("")
            logger.info("=" * 60)
            logger.info("STEP 3: Generating fallback visualizations")
            logger.info("=" * 60)

            for element_name, view_type in fallback_viz:
                viz_result = validator.run_viz(element_name, output_dir, view_type)
                viz_result.package_name = None
                report.viz_results.append(viz_result)

                if viz_result.status == "pass":
                    saved_to = (
                        viz_result.svg_path or viz_result.html_path
                        or viz_result.pdf_path or viz_result.png_path
                    )
                    logger.info("  ✅ Fallback Viz: %s (%s) → %s", element_name, view_type, saved_to)
                else:
                    for err in viz_result.errors:
                        logger.warning("  ⚠️ Fallback Viz: %s (%s): %s", element_name, view_type, err)

    except RuntimeError as e:
        logger.error("Kernel error: %s", e)
        report.errors += 1
    finally:
        validator.shutdown_kernel()


    report.total_time_s = round(time.time() - overall_start, 2)

    # -----------------------------------------------------------------------
    # Step 4: Generate outputs
    # -----------------------------------------------------------------------
    logger.info("")
    logger.info("=" * 60)
    logger.info("STEP 4: Generating outputs")
    logger.info("=" * 60)

    # JSON report
    json_path = os.path.join(output_dir, "validation_report.json")
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump(asdict(report), f, indent=2, default=str)
    logger.info("JSON report saved to %s", json_path)

    # Notebook
    try:
        nb_gen = NotebookGenerator()
        first_pkg = report.loading_order[0].lower() if report.loading_order else "validation"
        nb_name = f"{first_pkg}_validation.ipynb"
        nb_path = os.path.join(output_dir, nb_name)
        nb_gen.generate(report, nb_path)
    except RuntimeError as e:
        logger.error("Could not generate notebook: %s", e)
        nb_path = None

    # -----------------------------------------------------------------------
    # Final summary
    # -----------------------------------------------------------------------
    logger.info("")
    logger.info("=" * 60)
    logger.info("VALIDATION COMPLETE")
    logger.info("=" * 60)
    logger.info(
        "  Files: %d total, %d passed, %d failed, %d skipped",
        report.total_files, report.passed, report.failed, report.skipped,
    )
    logger.info("  Time:  %.1fs", report.total_time_s)
    logger.info("  Outputs:")
    logger.info("    - %s", json_path)
    if nb_path and os.path.isfile(nb_path):
        logger.info(
            "    - %s",
            nb_path,
        )
    for vr in report.viz_results:
        for path in (vr.svg_path, vr.html_path, vr.pdf_path, vr.png_path):
            if path:
                logger.info("    - %s", path)

    return report


