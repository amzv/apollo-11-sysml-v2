    def _make_code_cell(
        self,
        source: str,
        outputs: Optional[List] = None,
    ):
        """Create a code cell with optional pre-populated outputs."""
        cell = self.nbformat.v4.new_code_cell(source=source)
        if outputs:
            # Convert plain dicts to NotebookNode objects so nbformat.write()
            # can traverse them with attribute access (e.g. output.output_type)
            from nbformat import from_dict
            cell["outputs"] = [
                from_dict(o) if isinstance(o, dict) else o
                for o in outputs
            ]
        return cell

    def _build_cell_outputs(self, file_result: FileResult) -> List[Dict]:
        """Convert a FileResult into notebook cell output objects."""
        outputs = []

        # Stdout
        for text in file_result.stdout_output:
            outputs.append({
                "output_type": "stream",
                "name": "stdout",
                "text": text,
            })

        # Warnings (stderr)
        for text in file_result.warnings:
            outputs.append({
                "output_type": "stream",
                "name": "stderr",
                "text": text,
            })

        # Errors
        if file_result.errors:
            # Separate the ename/evalue from traceback
            ename = "ValidationError"
            evalue = file_result.errors[0] if file_result.errors else ""
            traceback = file_result.errors[1:] if len(file_result.errors) > 1 else []
            outputs.append({
                "output_type": "error",
                "ename": ename,
                "evalue": evalue,
                "traceback": traceback,
            })

        # If no outputs at all, add a success indicator
        if not outputs and file_result.status == "pass":
            outputs.append({
                "output_type": "stream",
                "name": "stdout",
                "text": f"✅ {file_result.package_name} loaded successfully "
                        f"({file_result.execution_time_s}s)\n",
            })

        return outputs

    def _build_viz_outputs(self, viz_result: VizResult) -> List[Dict]:
        """Convert a VizResult into notebook cell output objects."""
        outputs = []

        if viz_result.svg_path and os.path.isfile(viz_result.svg_path):
            with open(viz_result.svg_path, "r", encoding="utf-8") as f:
                svg_content = f.read()
            outputs.append({
                "output_type": "display_data",
                "data": {"image/svg+xml": svg_content, "text/plain": f"Diagram: {viz_result.element_name}"},
                "metadata": {},
            })
        elif viz_result.html_path and os.path.isfile(viz_result.html_path):
            with open(viz_result.html_path, "r", encoding="utf-8") as f:
                html_content = f.read()
            outputs.append({
                "output_type": "display_data",
                "data": {"text/html": html_content, "text/plain": f"Diagram: {viz_result.element_name}"},
                "metadata": {},
            })
        elif viz_result.pdf_path and os.path.isfile(viz_result.pdf_path):
            import base64
            with open(viz_result.pdf_path, "rb") as f:
                pdf_b64 = base64.b64encode(f.read()).decode("ascii")
            outputs.append({
                "output_type": "display_data",
                "data": {"application/pdf": pdf_b64, "text/plain": f"Diagram (PDF): {viz_result.element_name}"},
                "metadata": {},
            })
        elif viz_result.png_path and os.path.isfile(viz_result.png_path):
            import base64
            with open(viz_result.png_path, "rb") as f:
                png_b64 = base64.b64encode(f.read()).decode("ascii")
            outputs.append({
                "output_type": "display_data",
                "data": {"image/png": png_b64, "text/plain": f"Diagram (PNG): {viz_result.element_name}"},
                "metadata": {},
            })

        if viz_result.errors:
            outputs.append({
                "output_type": "error",
                "ename": "VisualizationError",
                "evalue": viz_result.errors[0],
                "traceback": viz_result.errors[1:],
            })

        return outputs

    def generate(
        self,
        report: ValidationReport,
        output_path: str,
    ) -> str:
        """Generate the .ipynb notebook and save it to output_path."""
        nb = self.nbformat.v4.new_notebook()

        # Set kernel metadata to SysML
        nb["metadata"]["kernelspec"] = {
            "display_name": "SysML",
            "language": "sysml",
            "name": KERNEL_NAME,
        }
        nb["metadata"]["language_info"] = {
            "codemirror_mode": "sysml",
            "file_extension": ".sysml",
            "mimetype": "text/x-sysml",
            "name": "SysML",
        }

        cells = []

        # --- Title cell ---
        pass_count = report.passed
        fail_count = report.failed + report.errors
        total = report.total_files
        status_emoji = "✅" if fail_count == 0 else "⚠️"
        model_name = report.loading_order[0] if report.loading_order else "SysML v2"

        cells.append(self._make_markdown_cell(
            f"# {status_emoji} {model_name} SysML v2 Model Validation\n\n"
            f"**Generated**: {report.timestamp}  \n"
            f"**Repository**: `{report.repo_path}`  \n"
            f"**Result**: {pass_count}/{total} files passed, "
            f"{fail_count} failed  \n"
            f"**Total Time**: {report.total_time_s:.1f}s\n\n"
            f"---\n\n"
            f"Each cell below contains one `.sysml` package file, loaded "
            f"in dependency order. Re-run this notebook in JupyterLab with "
            f"the SysML kernel to reproduce the validation."
        ))

        # Group visualizations by defining package
        viz_by_pkg = defaultdict(list)
        fallback_viz_results = []
        for vr in report.viz_results:
            if getattr(vr, "package_name", None):
                viz_by_pkg[vr.package_name].append(vr)
            else:
                fallback_viz_results.append(vr)

        # --- Model loading and visualization cells ---
        cells.append(self._make_markdown_cell(
            "## Model Loading and Visualizations\n\n"
            "The following cells load each package in topological dependency "
            "order. If a package has configured visualizations, they are run "
            "immediately after the package loads."
        ))

        for file_result in report.file_results:
            # Add a small markdown comment before each cell
            status = "✅" if file_result.status == "pass" else "❌"
            rel_path = os.path.relpath(file_result.file_path, report.repo_path)

            cells.append(self._make_markdown_cell(
                f"### {status} `{file_result.package_name}` — `{rel_path}`"
            ))

            # Read the source
            source = ""
            if os.path.isfile(file_result.file_path):
                try:
                    with open(
                        file_result.file_path, "r",
                        encoding="utf-8", errors="ignore"
                    ) as f:
                        source = f.read()
                except IOError:
                    source = f"// Could not read: {file_result.file_path}"

            outputs = self._build_cell_outputs(file_result)
            cells.append(self._make_code_cell(source, outputs))

            # If there are visualizations for this package, insert them right here
            pkg_name = file_result.package_name
            if pkg_name in viz_by_pkg:
                for viz_result in viz_by_pkg[pkg_name]:
                    status_viz = "✅" if viz_result.status == "pass" else "⚠️"
                    viz_cmd = f"%viz {viz_result.element_name} --view {viz_result.view_type}"
                    cells.append(self._make_markdown_cell(
                        f"#### {status_viz} Visualization: `{viz_cmd}`"
                    ))
                    outputs_viz = self._build_viz_outputs(viz_result)
                    cells.append(self._make_code_cell(
                        viz_cmd,
                        outputs_viz,
                    ))

        # --- Fallback Visualization cells ---
        if fallback_viz_results:
            cells.append(self._make_markdown_cell(
                "## Fallback Visualizations\n\n"
                "The following cells show visualizations that could not be mapped "
                "to a specific package file."
            ))

            for viz_result in fallback_viz_results:
                status_viz = "✅" if viz_result.status == "pass" else "⚠️"
                viz_cmd = f"%viz {viz_result.element_name} --view {viz_result.view_type}"
                cells.append(self._make_markdown_cell(
                    f"### {status_viz} `{viz_cmd}`"
                ))
                outputs_viz = self._build_viz_outputs(viz_result)
                cells.append(self._make_code_cell(
                    viz_cmd,
                    outputs_viz,
                ))

        # --- Summary cell ---
        summary_rows = []
        for fr in report.file_results:
            status = "✅ Pass" if fr.status == "pass" else "❌ Fail"
            err_summary = fr.errors[0][:60] + "..." if fr.errors else "—"
            summary_rows.append(
                f"| `{fr.package_name}` | {status} | {fr.execution_time_s}s | {err_summary} |"
            )

        cells.append(self._make_markdown_cell(
            "## Summary\n\n"
            "| Package | Status | Time | Error |\n"
            "|---------|--------|------|-------|\n"
            + "\n".join(summary_rows)
        ))

        nb["cells"] = cells

        with open(output_path, "w", encoding="utf-8") as f:
            self.nbformat.write(nb, f)

        logger.info("Notebook saved to %s", output_path)
        return output_path


