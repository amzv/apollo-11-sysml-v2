class SysMLKernelValidator:
    """
    Drives the SysML v2 Jupyter kernel via jupyter_client to validate
    .sysml files and capture visualization output.
    """

    def __init__(
        self,
        timeout: int = 60,
        startup_timeout: int = 120,
    ):
        self.timeout = timeout
        self.startup_timeout = startup_timeout
        self.km = None
        self.kc = None

    def start_kernel(self) -> None:
        """Start the SysML v2 Jupyter kernel."""
        try:
            from jupyter_client import KernelManager
        except ImportError:
            raise RuntimeError(
                "jupyter_client is not installed. "
                "Install it with: pip install jupyter_client"
            )

        logger.info("Starting SysML v2 Jupyter kernel...")
        self.km = KernelManager(kernel_name=KERNEL_NAME)

        try:
            self.km.start_kernel()
        except Exception as e:
            raise RuntimeError(
                f"Failed to start the '{KERNEL_NAME}' kernel. "
                f"Is it installed? (conda install -c conda-forge jupyter-sysml-kernel)\n"
                f"Error: {e}"
            )

        self.kc = self.km.client()
        self.kc.start_channels()

        try:
            self.kc.wait_for_ready(timeout=self.startup_timeout)
            logger.info("Kernel is ready.")
        except RuntimeError:
            self.shutdown_kernel()
            raise RuntimeError(
                f"Kernel did not become ready within {self.startup_timeout}s. "
                "Check that Java 17+ is installed and JAVA_HOME is set."
            )

    def shutdown_kernel(self) -> None:
        """Shut down the kernel cleanly."""
        if self.kc:
            self.kc.stop_channels()
        if self.km and self.km.is_alive():
            logger.info("Shutting down kernel...")
            self.km.shutdown_kernel(now=True)
            logger.info("Kernel shut down.")

    def execute_cell(self, code: str) -> Dict[str, Any]:
        """
        Execute a code cell in the kernel and collect all outputs.

        Returns a dict with:
            - "status": "ok" or "error"
            - "errors": list of error messages
            - "warnings": list of stderr messages
            - "stdout": list of stdout messages
            - "display_data": list of display_data payloads (for viz)
            - "execute_result": list of execute_result payloads
        """
        if not self.kc:
            raise RuntimeError("Kernel is not running. Call start_kernel() first.")

        msg_id = self.kc.execute(code)
        result = {
            "status": "ok",
            "errors": [],
            "warnings": [],
            "stdout": [],
            "display_data": [],
            "execute_result": [],
        }

        # Drain IOPub messages until we see the idle status for our execution
        while True:
            try:
                msg = self.kc.get_iopub_msg(timeout=self.timeout)
            except Exception:
                result["status"] = "error"
                result["errors"].append(
                    f"Timeout: no response within {self.timeout}s"
                )
                break

            msg_type = msg.get("msg_type", "")
            parent_id = msg.get("parent_header", {}).get("msg_id", "")
            content = msg.get("content", {})

            # Only process messages that belong to our execution
            if parent_id != msg_id:
                continue

            if msg_type == "error":
                result["status"] = "error"
                ename = content.get("ename", "Unknown")
                evalue = content.get("evalue", "")
                traceback_lines = content.get("traceback", [])
                error_msg = f"{ename}: {evalue}"
                result["errors"].append(error_msg)
                if traceback_lines:
                    result["errors"].extend(traceback_lines)

            elif msg_type == "stream":
                name = content.get("name", "")
                text = content.get("text", "")
                if name == "stderr":
                    result["warnings"].append(text)
                elif name == "stdout":
                    result["stdout"].append(text)
                
                # Check for compiler errors in stream output (common in SysML v2 kernel)
                for line in text.splitlines():
                    stripped = line.strip()
                    if stripped.startswith("ERROR:") or stripped.startswith("Error:"):
                        result["status"] = "error"
                        result["errors"].append(stripped)

            elif msg_type == "display_data":
                result["display_data"].append(content.get("data", {}))

            elif msg_type == "execute_result":
                data = content.get("data", {})
                result["execute_result"].append(data)
                
                # Check for compiler or reference errors in execute result (e.g. from %show)
                if "text/plain" in data:
                    plain_text = data["text/plain"]
                    for line in plain_text.splitlines():
                        stripped = line.strip()
                        if stripped.startswith("ERROR:") or stripped.startswith("Error:"):
                            result["status"] = "error"
                            result["errors"].append(stripped)

            elif msg_type == "status":
                if content.get("execution_state") == "idle":
                    break

        return result

    def publish_file(self, file_path: str) -> Dict[str, Any]:
        """
        Load a .sysml file into the kernel using the %publish magic.
        This registers the file's contents in the kernel's model namespace,
        making its definitions available for import by subsequent files.
        """
        kernel_path = _to_kernel_path(file_path)
        code = f"%publish {kernel_path}"
        logger.debug("Publishing: %s", code)
        return self.execute_cell(code)

    def show_element(self, element_name: str) -> Dict[str, Any]:
        """
        Run %show <element> to verify a package/element exists in the kernel.
        Returns the execute_cell result dict.
        """
        code = f"%show {element_name}"
        logger.debug("Verifying: %s", code)
        return self.execute_cell(code)

    def validate_file(self, package_name: str, file_path: str) -> FileResult:
        """
        Validate a single .sysml file by loading it into the kernel.

        Strategy:
          1. Execute the file's raw code (primary — registers elements in the
             kernel namespace so %viz and %show work)
          2. If raw execution fails, fall back to %publish (syntax check only)
          3. Run %show <PackageName> to verify the package registered
        """
        result = FileResult(
            package_name=package_name,
            file_path=file_path,
        )

        if not os.path.isfile(file_path):
            result.status = "error"
            result.errors.append(f"File not found: {file_path}")
            return result

        start_time = time.time()

        # Read the file content
        try:
            with open(file_path, "r", encoding="utf-8", errors="ignore") as f:
                code = f.read()
        except IOError as e:
            result.status = "error"
            result.errors.append(f"Could not read file: {e}")
            result.execution_time_s = round(time.time() - start_time, 3)
            return result

        # --- Step 1: Execute raw code (primary — registers namespace) ---
        cell_result = self.execute_cell(code)
        result.load_method = "raw"

        result.errors = cell_result["errors"]
        result.warnings = cell_result["warnings"]
        result.stdout_output = cell_result["stdout"]

        if cell_result["status"] != "ok" or cell_result["errors"]:
            # --- Fallback: try %publish (syntax validation only) ---
            logger.warning(
                "Raw execution failed for %s, falling back to %%publish",
                package_name,
            )
            publish_result = self.publish_file(file_path)
            result.load_method = "publish"

            if publish_result["status"] == "ok" and not publish_result["errors"]:
                # %publish succeeded — file is syntactically valid but
                # elements won't be in namespace for %viz
                result.errors = []
                result.warnings = publish_result["warnings"] + [
                    "Loaded via %publish (syntax only) — elements not in namespace for %viz"
                ]
                result.stdout_output = publish_result["stdout"]
            else:
                # Both methods failed — keep the original raw execution errors
                result.status = "fail"
                result.execution_time_s = round(time.time() - start_time, 3)
                return result

        # --- Step 2: Verify with %show ---
        show_result = self.show_element(package_name)
        result.show_output = show_result.get("stdout", []) + [
            data.get("text/plain", "")
            for data in show_result.get("execute_result", [])
            if "text/plain" in data
        ]

        if show_result["errors"]:
            # %show failed — element not found in namespace
            show_err_text = "; ".join(show_result["errors"])
            if result.load_method == "publish":
                # Expected — %publish doesn't register namespace
                result.warnings.append(
                    f"%show {package_name} not available (loaded via %publish)"
                )
            else:
                # Unexpected — raw execution should register namespace
                result.warnings.append(
                    f"%show {package_name} returned errors: {show_err_text}"
                )
            logger.debug(
                "%%show %s: %s",
                package_name,
                show_result["errors"][0][:100] if show_result["errors"] else "unknown",
            )

        result.execution_time_s = round(time.time() - start_time, 3)

        if not result.errors:
            result.status = "pass"
        else:
            result.status = "fail"

        return result

    def run_viz(
        self, element_name: str, output_dir: str, view_type: str = "tree",
    ) -> VizResult:
        """Execute %viz for an element and save the output."""
        viz_result = VizResult(element_name=element_name, view_type=view_type)

        code = f"%viz {element_name} --view {view_type}"
        logger.info("Running: %s", code)

        cell_result = self.execute_cell(code)

        # Debug: log all data received so we can see what format viz comes in
        logger.debug(
            "  viz status=%s, errors=%d, display_data=%d, execute_result=%d, stdout=%d",
            cell_result["status"],
            len(cell_result["errors"]),
            len(cell_result["display_data"]),
            len(cell_result["execute_result"]),
            len(cell_result["stdout"]),
        )
        for i, dd in enumerate(cell_result["display_data"]):
            logger.debug("  display_data[%d] keys: %s", i, list(dd.keys()))
            for key, val in dd.items():
                preview = str(val)[:150] if isinstance(val, str) else str(type(val))
                logger.debug("    %s: %s", key, preview)
        for i, er in enumerate(cell_result["execute_result"]):
            logger.debug("  execute_result[%d] keys: %s", i, list(er.keys()))
            for key, val in er.items():
                preview = str(val)[:150] if isinstance(val, str) else str(type(val))
                logger.debug("    %s: %s", key, preview)
        for i, so in enumerate(cell_result["stdout"]):
            logger.debug("  stdout[%d]: %s", i, so[:200])
        for i, w in enumerate(cell_result["warnings"]):
            logger.debug("  stderr[%d]: %s", i, w[:200])

        if cell_result["errors"]:
            viz_result.status = "fail"
            viz_result.errors = cell_result["errors"]
            return viz_result

        # Collect all data payloads from both display_data and execute_result
        all_data_payloads = (
            cell_result["display_data"] + cell_result["execute_result"]
        )

        # Search for visualization output across all MIME types
        saved = False
        for payload in all_data_payloads:
            # Priority 1: SVG (text, directly writable)
            if "image/svg+xml" in payload and not saved:
                svg_content = payload["image/svg+xml"]
                out_path = os.path.join(
                    output_dir, f"viz_{element_name}_{view_type}.svg"
                )
                with open(out_path, "w", encoding="utf-8") as f:
                    f.write(svg_content)
                viz_result.svg_path = out_path
                saved = True
                logger.info("Saved SVG to %s", out_path)

            # Priority 2: HTML
            if "text/html" in payload and not saved:
                html_content = payload["text/html"]
                out_path = os.path.join(
                    output_dir, f"viz_{element_name}_{view_type}.html"
                )
                with open(out_path, "w", encoding="utf-8") as f:
                    f.write(html_content)
                viz_result.html_path = out_path
                saved = True
                logger.info("Saved HTML to %s", out_path)

            # Priority 3: PDF (base64-encoded binary)
            if "application/pdf" in payload and not saved:
                import base64
                pdf_b64 = payload["application/pdf"]
                out_path = os.path.join(
                    output_dir, f"viz_{element_name}_{view_type}.pdf"
                )
                with open(out_path, "wb") as f:
                    f.write(base64.b64decode(pdf_b64))
                viz_result.pdf_path = out_path
                saved = True
                logger.info("Saved PDF to %s", out_path)

            # Priority 4: PNG (base64-encoded binary)
            if "image/png" in payload and not saved:
                import base64
                png_b64 = payload["image/png"]
                out_path = os.path.join(
                    output_dir, f"viz_{element_name}_{view_type}.png"
                )
                with open(out_path, "wb") as f:
                    f.write(base64.b64decode(png_b64))
                viz_result.png_path = out_path
                saved = True
                logger.info("Saved PNG to %s", out_path)

            if saved:
                break

        # Also check stdout for any text-based diagram output (e.g., PlantUML text)
        if not saved and cell_result["stdout"]:
            stdout_text = "".join(cell_result["stdout"])
            if stdout_text.strip():
                out_path = os.path.join(
                    output_dir, f"viz_{element_name}_{view_type}.txt"
                )
                with open(out_path, "w", encoding="utf-8") as f:
                    f.write(stdout_text)
                logger.info("Saved text output to %s", out_path)
                saved = True

        if saved:
            viz_result.status = "pass"
        else:
            viz_result.status = "fail"
            viz_result.errors.append(
                "No visualization output received from %viz command. "
                "Check that the element is a package name (not a part def), "
                "and the view type is valid (tree, interconnection, Action, State)."
            )

        return viz_result


