def main():
    parser = argparse.ArgumentParser(
        description="Validate a SysML v2 model using the Jupyter kernel.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=(
            "Examples:\n"
            "  python scripts/validate_model.py\n"
            "  python scripts/validate_model.py --verbose\n"
            "  python scripts/validate_model.py --viz-elements SaturnV,Apollo11Mission\n"
            "  python scripts/validate_model.py --repo-path . --output-dir scripts/output\n"
        ),
    )

    # Auto-detect repo path: script is in scripts/, repo is parent
    default_repo = os.path.abspath(
        os.path.join(os.path.dirname(__file__), "..")
    )
    default_output = os.path.join(
        os.path.dirname(__file__), "output"
    )

    parser.add_argument(
        "--repo-path",
        default=default_repo,
        help=f"Path to the SysML v2 repository root (default: {default_repo})",
    )
    parser.add_argument(
        "--output-dir",
        default=default_output,
        help=f"Output directory for report, notebook, and diagrams (default: {default_output})",
    )
    # Format default viz for display
    default_viz_str = ",".join(
        f"{name}:{view}" for name, view in DEFAULT_VIZ_ELEMENTS
    )

    parser.add_argument(
        "--viz-elements",
        default=default_viz_str,
        help=(
            "Comma-separated list of element:view pairs to visualize with %%viz. "
            "Format: 'ElementName:ViewType' (e.g., 'SaturnV:tree,ApolloSpacecraft:interconnection'). "
            "If no ':ViewType' is given, the --viz-view default is used. "
            f"(default: {default_viz_str})"
        ),
    )
    parser.add_argument(
        "--viz-view",
        default="tree",
        help="Default view type for %%viz when not specified per-element (default: tree)",
    )
    parser.add_argument(
        "--no-viz",
        action="store_true",
        help="Skip visualization step entirely",
    )
    parser.add_argument(
        "--timeout",
        type=int,
        default=60,
        help="Per-cell execution timeout in seconds (default: 60)",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Resolve dependencies and print loading order without starting the kernel",
    )
    parser.add_argument(
        "--no-continue-on-error",
        action="store_true",
        help="Stop immediately when a file fails (default: continue and skip dependents)",
    )
    parser.add_argument(
        "--target-package",
        default=None,
        help="Specific package name or file path to validate. Only this and its dependencies will be loaded.",
    )
    parser.add_argument(
        "--verbose", "-v",
        action="store_true",
        help="Enable debug-level logging",
    )

    args = parser.parse_args()

    # Configure logging
    log_level = logging.DEBUG if args.verbose else logging.INFO
    logging.basicConfig(
        level=log_level,
        format="%(asctime)s [%(levelname)s] %(message)s",
        datefmt="%H:%M:%S",
    )

    # Parse viz elements: support "Element:View" or just "Element" (uses --viz-view default)
    viz_elements = []
    if not args.no_viz:
        for entry in args.viz_elements.split(","):
            entry = entry.strip()
            if not entry:
                continue
            if ":" in entry:
                name, view = entry.split(":", 1)
                viz_elements.append((name.strip(), view.strip()))
            else:
                viz_elements.append((entry, args.viz_view))

    # Run validation
    report = run_validation(
        repo_path=args.repo_path,
        output_dir=args.output_dir,
        viz_elements=viz_elements,
        timeout=args.timeout,
        dry_run=args.dry_run,
        continue_on_error=not args.no_continue_on_error,
        target_package=args.target_package,
    )

    # Exit with non-zero code if any files failed
    if report.failed > 0 or report.errors > 0:
        sys.exit(1)


if __name__ == "__main__":
    main()
