class SysMLDependencyResolver:
    """
    Resolves the loading order for .sysml files by parsing import statements
    and performing a topological sort of the dependency graph.
    """

    IMPORT_PATTERN = re.compile(
        r'(?:private\s+)?import\s+([\w]+)(?:::[\w*]+)*'
    )
    # Matches: PackageName::ElementName (inline references)
    INLINE_PATTERN = re.compile(
        r'\b([\w]+)::'
    )

    def __init__(self, repo_path: str):
        self.repo_path = os.path.abspath(repo_path)
        self.package_index: Dict[str, str] = {}  # package_name -> relative_path
        self.dep_graph: Dict[str, set] = defaultdict(set)
        self._load_meta_index()

    def _load_meta_index(self) -> None:
        """Load the package-to-file index from .meta.json."""
        meta_path = os.path.join(self.repo_path, ".meta.json")
        if os.path.isfile(meta_path):
            with open(meta_path, "r", encoding="utf-8") as f:
                meta = json.load(f)
            self.package_index = dict(meta.get("index", {}))
            logger.info(
                "Loaded %d packages from .meta.json", len(self.package_index)
            )
        else:
            logger.warning(
                ".meta.json not found at %s — falling back to file discovery",
                meta_path,
            )
            self._discover_packages()

    def _discover_packages(self) -> None:
        """Fallback: discover packages by walking the directory tree."""
        for root, _, files in os.walk(self.repo_path):
            for fname in files:
                if fname.endswith(".sysml"):
                    pkg_name = fname.replace(".sysml", "")
                    rel_path = os.path.relpath(
                        os.path.join(root, fname), self.repo_path
                    )
                    self.package_index[pkg_name] = rel_path

    def get_imports(self, file_path: str) -> List[str]:
        """Extract import package names from a .sysml file."""
        if not os.path.isfile(file_path):
            return []
        try:
            with open(file_path, "r", encoding="utf-8", errors="ignore") as f:
                content = f.read()
            
            # Find explicit imports
            matches = self.IMPORT_PATTERN.findall(content)
            
            # Also find inline namespace references (e.g. CapabilitiesPackage::Element)
            inline_matches = self.INLINE_PATTERN.findall(content)
            
            # Deduplicate while preserving order
            seen = set()
            unique = []
            for m in matches + inline_matches:
                if m not in seen:
                    seen.add(m)
                    unique.append(m)
            return unique
        except IOError as e:
            logger.error("Error reading %s: %s", file_path, e)
            return []

    def build_dependency_graph(self) -> None:
        """Build the directed dependency graph from all indexed packages."""
        for pkg_name, rel_path in self.package_index.items():
            abs_path = os.path.join(self.repo_path, rel_path)
            imports = self.get_imports(abs_path)
            project_deps = set()
            for imp in imports:
                if imp in self.package_index and imp != pkg_name:
                    project_deps.add(imp)
                elif imp not in SYSML_V2_STANDARD_LIBRARIES:
                    logger.debug(
                        "Package '%s' imports unknown '%s' (may be stdlib)",
                        pkg_name, imp,
                    )
            self.dep_graph[pkg_name] = project_deps
            # Ensure all nodes exist even if they have no deps
            for dep in project_deps:
                if dep not in self.dep_graph:
                    self.dep_graph[dep] = set()

    def topological_sort(self) -> List[str]:
        """
        Perform a topological sort (Kahn's algorithm) on the dependency graph.
        Returns packages in loading order (dependencies first).
        """
        # Compute in-degree for each node
        in_degree: Dict[str, int] = {
            node: 0 for node in self.dep_graph
        }
        for node, deps in self.dep_graph.items():
            for dep in deps:
                in_degree[node] = in_degree.get(node, 0)
                # dep is a dependency OF node, meaning node depends on dep
                # So we don't increment dep's in-degree here

        # Recalculate: in_degree[X] = number of packages that X depends on?
        # No — in_degree[X] = number of edges pointing INTO X
        # An edge from A to B means "A depends on B", so B must load first.
        # That means B has an incoming edge from A in the "depends on" graph.
        # But for topological sort, we want to reverse: B should come before A.
        # So we build a "dependency-of" (reverse) graph.

        # Reverse the graph: if A depends on B, add edge B -> A
        reverse_graph: Dict[str, set] = defaultdict(set)
        in_degree_correct: Dict[str, int] = {
            node: 0 for node in self.dep_graph
        }
        for node, deps in self.dep_graph.items():
            in_degree_correct[node] = len(deps)
            for dep in deps:
                reverse_graph[dep].add(node)
                if dep not in in_degree_correct:
                    in_degree_correct[dep] = 0

        # Kahn's algorithm
        queue = [
            node for node, deg in in_degree_correct.items() if deg == 0
        ]
        queue.sort()  # deterministic ordering for same-depth nodes
        result = []

        while queue:
            node = queue.pop(0)
            result.append(node)
            for neighbor in sorted(reverse_graph.get(node, set())):
                in_degree_correct[neighbor] -= 1
                if in_degree_correct[neighbor] == 0:
                    queue.append(neighbor)

        if len(result) != len(self.dep_graph):
            missing = set(self.dep_graph.keys()) - set(result)
            logger.error(
                "Circular dependency detected! Could not sort: %s", missing
            )
            # Append remaining in arbitrary order
            for m in sorted(missing):
                result.append(m)

        return result

    def resolve(self) -> List[Tuple[str, str]]:
        """
        Build the graph and return an ordered list of (package_name, abs_path)
        tuples ready for loading into the kernel.
        """
        self.build_dependency_graph()
        order = self.topological_sort()

        result = []
        for pkg_name in order:
            if pkg_name in self.package_index:
                abs_path = os.path.join(
                    self.repo_path, self.package_index[pkg_name]
                )
                result.append((pkg_name, abs_path))
            else:
                logger.warning(
                    "Package '%s' is in the graph but has no file mapping",
                    pkg_name,
                )
        return result


